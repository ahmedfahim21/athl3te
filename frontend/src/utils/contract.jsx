const CONTRACT_ADDRESS = process.env.NEXT_PUBLIC_CONTRACT_ADDRESS;

if (!CONTRACT_ADDRESS) {
  throw new Error("CONTRACT_ADDRESS not found in environment variables");
}

export async function mintNFT(recipient, metadataURI) {
  if (!window.ethereum) throw new Error("No wallet found!");

  try {
    let provider;
    if (typeof window.ethereum.providers === "object") {
      const providers = window.ethereum.providers;
      const coinbaseProvider = providers.find((p) => p.isCoinbaseWallet);
      provider = new ethers.BrowserProvider(
        coinbaseProvider || window.ethereum
      );
    } else {
      provider = new ethers.BrowserProvider(window.ethereum);
    }

    console.log("Got provider:", provider);
    const signer = await provider.getSigner();
    console.log("Got signer:", signer);

    const contract = new ethers.Contract(CONTRACT_ADDRESS, contractABI, signer);
    console.log("Contract initialized");

    console.log("Minting with URI:", metadataURI);
    const tx = await contract.mintWithURI(metadataURI);
    console.log("Transaction sent:", tx);

    const receipt = await tx.wait();
    console.log("Transaction confirmed:", receipt);

    return receipt.hash;
  } catch (error) {
    console.error("NFT Minting Error:", error);
    throw error;
  }
}
